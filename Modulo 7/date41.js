/**
 * 
 * O objeto Date é um construtor de datas e horários. Existem quatro
 * sintaxes para criar um objeto Date, vejamos as sintaxes:
 * 
 * var data = new Date()
 * var data = new Date(milissegundos)
 * var data = new Date(data_no_formato_string)
 * var data = new Date(ano, mês, dia, hora, minuto, segundo, 
 * milissegundo)
 * 
 * getDate()
 *  Retorna o dia do mês (1 a 31) de um objeto Date
 * 
 * getDay()
 *  Retorna o dia da semana(0 - 6) de um objeto Date
 * 
 * getFullYear()
 *  Retorna o ano, com quatro dígito, de um objeto Date
 * 
 * getHours()
 *  Retorna a hora (0 - 23) de um objeto Date
 * 
 * getMilliseconds()
 *  Retorna o número de milissegundos (0 - 999) de um objeto Date
 * 
 * getMinutes()
 *  Retorna os minutos (0 - 59) de um objeto Date
 * 
 * getMonth()
 *  Retorna o mês (0 - 11) de um objeto Date
 * 
 * getSeconds()
 *  Retorna os segundos (0 - 59) de um objeto Date
 * 
 * getTime()
 *  Retorna o número de milissegundos transcorridos entre um Date
 * e a data base 01 de janeiro de 1970
 * 
 * getTimezoneOffset()
 *  Retorna a diferença em minutos (0 - 59) entre a hora local e a 
 * hora GMT
 * 
 * getUTFDate(), etc...
 *  Retorna o dia do mês(1 - 31) de um objeto date com base no GMT
 * 
 * Todos os métodos sets correspondentes aos getters
 * 
 * 
 */
